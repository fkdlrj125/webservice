# 논리형
print('정수 0의 논리값 >> ', bool(0))
print('정수 1의 논리값 >> ', bool(1))
print('정수 100의 논리값 >> ', bool(100))
print('정수 -5의 논리값 >> ', bool(-5))

# 문자열 -> 논리
print('문자열 Hi의 논리값', bool('Hi'))
print('빈 문자열의 논리값', bool(''))

# 리스트 -> 논리
print('리스트 [2, 4]의 논리값 >> ',bool([2,4]))
print('빈 리스트의 논리값 >> ',bool([]))

